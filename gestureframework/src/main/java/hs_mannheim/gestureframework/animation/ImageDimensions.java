package hs_mannheim.gestureframework.animation;

public class ImageDimensions {
    int mImageWidth, mImageHeight;

    public ImageDimensions(int width, int height){
        mImageWidth = width;
        mImageHeight = height;
    }
    public int getImageHeight() {
        return mImageHeight;
    }

    public int getImageWidth() {
        return mImageWidth;
    }
}